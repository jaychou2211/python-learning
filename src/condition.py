# conditions and conditional operators.

# So a condition is simply something in Python 
# that evaluates or compares to variables or data types 
# and returns to us either a true or false value based on the comparison.

x = 'hello';
y = 'hello';

print(x == y) # True
print(x != y) # False

# Every single character inside a Python is represented by
# what's known as an ASCII code.
print('a' > 'Z') # True
print(ord('a')) # 97
print(ord('Z')) # 90
# Just understand that we're comparing the ordinal value of the strings.

